    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(txtamount.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Please enter the amount");
            txtamount.requestFocus();
        }
        else if(txtdate.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Please enter the date");
            txtdate.requestFocus();
        }
        else if(txtcategory.getSelectedIndex() == 0){
            JOptionPane.showMessageDialog(this, "Please choose the category");
            txtcategory.requestFocus();
        }
        else{
            String username = LogIn.SharedData.username;
            String sqlString = "INSERT INTO INCOME(AMOUNT,DATE,DESCRIPTION,CATEGORY,USERNAME)VALUES(?,?,?,?,?) ";
            try {
                pst = conn.prepareStatement(sqlString);
                pst.setString(5, username);
                pst.setString(1, txtamount.getText());
                pst.setString(2, txtdate.getText());
                pst.setString(3, txtdescription.getText());
                pst.setString(4, txtcategory.getSelectedItem().toString());
                pst.executeUpdate();
                JOptionPane.showMessageDialog(this, "Data added");
                loaddetails();
            } catch (SQLException ex) {
                Logger.getLogger(Income.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, ex);
            }
            
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

