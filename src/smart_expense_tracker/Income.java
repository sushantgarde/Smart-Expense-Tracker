    public final void loaddetails(){
        DefaultTableModel model = new DefaultTableModel();
//        model.addColumn("Sr no");
        model.addColumn("Income");
        model.addColumn("Date");
        model.addColumn("Category");
        model.addColumn("Description");
        
        String sql = "SELECT AMOUNT, DATE, CATEGORY, DESCRIPTION FROM INCOME WHERE USERNAME=?";
        try {
            String username = LogIn.SharedData.username;
            pst = conn.prepareStatement(sql);
            pst.setString(1, username);
            rs = pst.executeQuery();
            
            while (rs.next()) {
                model.addRow(new Object[]{
//                        rs.getString("ID"),
                        rs.getString("AMOUNT"),
                        rs.getString("DATE"),
                        rs.getString("CATEGORY"),
                        rs.getString("DESCRIPTION")
                });
            }
             jTable1.setModel(model);
        } catch (SQLException ex) {
            Logger.getLogger(Income.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }
    
